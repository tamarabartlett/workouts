import { writeFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';
import dotenv from 'dotenv';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outPath = join(root, 'src', 'auth-secrets.generated.ts');

const quiet = { quiet: true };
dotenv.config({ path: join(root, '.env'), ...quiet });
dotenv.config({ path: join(root, '.env.local'), override: true, ...quiet });

const user = process.env.ALLOWED_USERNAME ?? '';
const hash = process.env.ALLOWED_PASSWORD_HASH ?? '';

const mustHaveSecrets =
  process.env.VERCEL === '1' ||
  process.env.CI === 'true' ||
  process.env.FORCE_AUTH_SECRETS === '1';

if (mustHaveSecrets && (!user || !hash)) {
  console.error(
    'Missing ALLOWED_USERNAME or ALLOWED_PASSWORD_HASH. Add them in Vercel environment variables (build) or .env.local for local builds.',
  );
  process.exit(1);
}

const content = `// Generated by scripts/generate-auth-secrets.mjs — do not edit.
export const ALLOWED_USERNAME = ${JSON.stringify(user)};
export const ALLOWED_PASSWORD_HASH = ${JSON.stringify(hash)};
`;

writeFileSync(outPath, content, 'utf8');
